// 根据任务标签获取对应的脚本
export const getScriptByTaskType = (taskType) => {
  return brainstormingScripts[taskType] || brainstormingScripts.default
}

// 根据任务描述智能匹配场景类型
export const matchTaskType = (taskTitle, taskDescription = '') => {
  const text = (taskTitle + ' ' + taskDescription).toLowerCase()

  if (text.includes('功能') || text.includes('feature') || text.includes('add') || text.includes('create') || text.includes('实现')) {
    return 'feature'
  }
  if (text.includes('bug') || text.includes('修复') || text.includes('fix') || text.includes('问题') || text.includes('异常')) {
    return 'bug'
  }
  if (text.includes('性能') || text.includes('优化') || text.includes('performance') || text.includes('optim')) {
    return 'performance'
  }
  if (text.includes('需求') || text.includes('requirement') || text.includes('分析') || text.includes('分析')) {
    return 'requirement'
  }

  return 'default'
}
